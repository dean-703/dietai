package com.courtney.dietai.profile;

public class Targets {
    private double bmr;
    private double tdee;
    private double calorieTarget;
    private double proteinTargetG;
    private double carbsTargetG;
    private double fatTargetG;
    private double fiberTargetG;
    private double sodiumTargetMg;

    public double getBmr() { return bmr; }
    public void setBmr(double bmr) { this.bmr = bmr; }

    public double getTdee() { return tdee; }
    public void setTdee(double tdee) { this.tdee = tdee; }

    public double getCalorieTarget() { return calorieTarget; }
    public void setCalorieTarget(double calorieTarget) { this.calorieTarget = calorieTarget; }

    public double getProteinTargetG() { return proteinTargetG; }
    public void setProteinTargetG(double proteinTargetG) { this.proteinTargetG = proteinTargetG; }

    public double getCarbsTargetG() { return carbsTargetG; }
    public void setCarbsTargetG(double carbsTargetG) { this.carbsTargetG = carbsTargetG; }

    public double getFatTargetG() { return fatTargetG; }
    public void setFatTargetG(double fatTargetG) { this.fatTargetG = fatTargetG; }

    public double getFiberTargetG() { return fiberTargetG; }
    public void setFiberTargetG(double fiberTargetG) { this.fiberTargetG = fiberTargetG; }

    public double getSodiumTargetMg() { return sodiumTargetMg; }
    public void setSodiumTargetMg(double sodiumTargetMg) { this.sodiumTargetMg = sodiumTargetMg; }
}